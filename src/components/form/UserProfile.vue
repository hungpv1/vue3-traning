import request from '@/request';
// import { createToast } from 'vue-toastification';

export default {
    mounted() {
        this.setUserInfo();
    },
    data() {
        return {
            fullName: '',
            email: '',
            phone: '',
            mobile: '',
            address: '',
            website: '',
            github: '',
            twitter: '',
            instargram: '',
            facebook: ''
        }
    },
    methods: {
        setUserInfo() {
            request.get('/user/current').then((res) => {
                const user_info = res.data.user_info;
                this.fullName = user_info.name;
                this.email = user_info.email;
                this.phone = user_info.phone;
                this.mobile = user_info.mobile;
                this.address = user_info.address;
                let socialInfo = user_info.social_info;
                socialInfo = JSON.parse(socialInfo);
                this.website = socialInfo.website;
                this.github = socialInfo.github;
                this.twitter = socialInfo.twitter;
                this.instargram = socialInfo.instargram;
                this.facebook = socialInfo.facebook;
            });
        },
        formSubmit() {
            const data = {
                name: this.$refs.fullName.value,
                email: this.$refs.email.value,
                phone: this.$refs.phone.value,
                mobile: this.$refs.mobile.value,
                address: this.$refs.address.value
            }
            request.put('user/current', data).then(() => {
                this.$toast.success('Update Successed!');
                this.setUserInfo();
            }).catch(error => {
                const response = error.response.data
                console.log(response.errors.name)
                this.$toast.error(response.message);
                response.errors.name.forEach(message => {
                    this.$toast.error(message);
                })
            });
        },
        updateSocial() {
            const data = {
                website: this.$refs.website.value,
                github: this.$refs.github.value,
                instargram: this.$refs.instargram.value,
                facebook: this.$refs.facebook.value,
                twitter: this.$refs.twitter.value
            }
            request.put('user/currentSocial', data).then(() => {
                this.$toast.success('Update Successed!');
                this.setUserInfo();
            }).catch(error => {
                this.$toast.error(error.message);
            });
        }
    }
}
